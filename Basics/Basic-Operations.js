// You can perform basic arithmetic and logical operations in JavaScript.
// Arithmetic operations:

let sum = 10 + 5; // Addition
console.log("Sum of two number is : " + sum);

let difference = 10 - 5; // Subtraction
console.log("difference of two number is : " + difference);

let product = 10 * 5; // Multiplication
console.log("product of two number is : " + product);

let quotient = 10 / 5; // Division
console.log("division of two number is : " + quotient);

let remainder = 10 % 3; // Modulus (remainder of division)
console.log("remainder of two number is : " + remainder);

// Comparison Operators:
console.log("comparison operators: 'isEqual' ");
let isEqual = 5 == 5; // True if values are equal
console.log("5 == 5 : " + isEqual);

console.log("comparison operators: 'isNotEqual' ");
let isNotEqual = 5 != 3; // True if values are not equal
console.log("5 != 3 : " + isNotEqual);

console.log("comparison operators: 'isGreaterThan' ");
let isGreaterThan = 5 > 3; // True if 5 is greater than 3
console.log("5 > 3 : " + isGreaterThan);

console.log("comparison operators: 'isLessThan' ");
let isLessThan = 3 < 5; // True if 3 is less than 5
console.log("3 < 5 : " + isLessThan);

console.log("comparison operators: 'isEqualStrict' ");
let isEqualStrict = 5 === "5"; // Strict equality (values and types must match)
console.log("5 === '5' : " + isEqualStrict);

// Logical Operators:
console.log("Logical AND example!");
let andResult = true && false; // Logical AND
console.log("Logical AND result for 'true' && 'false' " + andResult);

console.log("Logical OR example!");
let orResult = true || false; // Logical OR
console.log("Logical OR result for 'true'|| 'false' " + orResult);

console.log("Logical NOT example!");
let notResult = !true; // Logical NOT
console.log("Logical NOT result for '!true'" + notResult);
